interface Figure
{
	public function draw();
}
class Square implements Figure
{
	public function draw()
	{
		return "Shape Square";
	}

}
class Circle implements Figure
{
	public function draw()
	{
		return "Shape Circle";
	}
}
class RedColor implements Figure
{
	protected $figure;

	public function __construct(Figure $figure)
	{
		$this->figure = $figure;
	}

	public function draw(): string
	{
		return 'Red colored '. $this->figure->draw();
	}
}
class BlueColor implements Figure
{
	protected $figure;

	public function __construct(Figure $figure)
	{
		$this->figure = $figure;
	}

	public function draw(): string
	{
		return 'Blue colored '. $this->figure->draw();
	}
}

